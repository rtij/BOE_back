    public function FindByTypeAndIssup($value1, $value2){
        return $this->createQueryBuilder('a')
        ->andWhere('a.type = :val1')
        ->setParameter('val1', $value1)
        ->andWhere('a.issup = :val2')
        ->setParameter('val2', $value2)
        ->getQuery()
        ->getResult();
    }

    public function getLast(){
        $sql = "SELECT Max(a.idutilisateur) from App:Utilisateur a";
        return $this->getEntityManager()->createQuery($sql)->getOneOrNullResult();
    }

